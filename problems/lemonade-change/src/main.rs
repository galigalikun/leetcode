struct Solution;
impl Solution {
    pub fn lemonade_change(bills: Vec<i32>) -> bool {
        let mut amount = 0;
        for bill in bills {
            let sub = bill - 5;
            if sub > 0 {
                if amount < sub {
                    return false;
                }
                amount -= sub;
            }
            amount += 5;
        }
        return true;
    }
}
