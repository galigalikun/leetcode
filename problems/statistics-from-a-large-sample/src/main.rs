struct Solution;
impl Solution {
    pub fn sample_stats(count: Vec<i32>) -> Vec<f64> {
        let mut min = 256;
        let mut max = 0;
        let mut sum = 0;
        let mut mode = 0;
        let mut mode_count = 0;
        let mut median = 0;
        let mut count_sum = 0;
        for i in 0..count.len() {
            if count[i] > 0 {
                min = std::cmp::min(min, i);
                max = std::cmp::max(max, i);
                sum += i as i32 * count[i];
                count_sum += count[i];
                if count[i] > mode_count {
                    mode_count = count[i];
                    mode = i;
                }
            }
        }
        let mut median_sum = 0;
        for i in 0..count.len() {
            if count[i] > 0 {
                median_sum += count[i];
                if median_sum >= count_sum / 2 {
                    median = i;
                }
            }
        }
        return vec![
            min as f64,
            max as f64,
            sum as f64 / count_sum as f64,
            median as f64,
            mode as f64,
        ];
    }
}
